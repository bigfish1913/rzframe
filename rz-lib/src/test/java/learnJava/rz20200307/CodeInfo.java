package learnJava.rz20200307;

public class CodeInfo {
	
	private int attributeNameIndex;
	private int attributeLenght;
	private int maxStack;
	private int maxLocals;
	private int codeLength;
	private String code;
	private int exceptionTableLength;
	private String exceptionTable;
	private int attributeCount;
	private String attributeInfo;
	
	public int getAttributeNameIndex() {
		return attributeNameIndex;
	}
	
	public void setAttributeNameIndex(int attributeNameIndex) {
		this.attributeNameIndex = attributeNameIndex;
	}
	
	public int getAttributeLenght() {
		return attributeLenght;
	}
	
	public void setAttributeLenght(int attributeLenght) {
		this.attributeLenght = attributeLenght;
	}
	
	public int getMaxStack() {
		return maxStack;
	}
	
	public void setMaxStack(int maxStack) {
		this.maxStack = maxStack;
	}
	
	public int getMaxLocals() {
		return maxLocals;
	}
	
	public void setMaxLocals(int maxLocals) {
		this.maxLocals = maxLocals;
	}
	
	public int getCodeLength() {
		return codeLength;
	}
	
	public void setCodeLength(int codeLength) {
		this.codeLength = codeLength;
	}
	
	public String getCode() {
		return code;
	}
	
	public void setCode(String code) {
		this.code = code;
	}
	
	public int getExceptionTableLength() {
		return exceptionTableLength;
	}
	
	public void setExceptionTableLength(int exceptionTableLength) {
		this.exceptionTableLength = exceptionTableLength;
	}
	
	public String getExceptionTable() {
		return exceptionTable;
	}
	
	public void setExceptionTable(String exceptionTable) {
		this.exceptionTable = exceptionTable;
	}
	
	public int getAttributeCount() {
		return attributeCount;
	}
	
	public void setAttributeCount(int attributeCount) {
		this.attributeCount = attributeCount;
	}
	
	public String getAttributeInfo() {
		return attributeInfo;
	}
	
	public void setAttributeInfo(String attributeInfo) {
		this.attributeInfo = attributeInfo;
	}
}
